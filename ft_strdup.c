#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		j;
	char	*p;

	j = 0;
	while (s1[j])
		++j;
	if (!(p = (char*)malloc(sizeof(char) * (j + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
